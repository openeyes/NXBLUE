'use strict';
require('dotenv').config();

/**
 * Build Event iconset (raster PNG spritesheet)
 *
 * Event icons must be 76px x 76px
 * They are used by CSS at 50% and at 25% e.g. 38px & 19px
 * - output:
 * - PNG: dist/img/oe-i-e-76x76-{tag}.png
 * - SCSS: src/sass/openeyes/ui/iconography/oe-i-e/
 *         _oe-i-e-base.scss
 *         _oe-i-e-spritesheet.scss
 */

const chalk = require('chalk'); // nice stdout with chalk! ;)
const green = chalk.bold.green;
const red = chalk.bold.red;
const log = console.log;

// modules
const fs = require('fs');
const rm = require('fs/promises').rm;
const fg = require('fast-glob');
const Spritesmith = require('spritesmith');
const Jimp = require('jimp');

/**
 * 1) Remove the current PNG spritesheet
 * 2) Spritesmith will build an image and associated coordinate mapping
 * 3a) Jimp uses the image buffer to create the PNG spritesheet
 * 3b) Write out the updated SCSS files
 *
 * Cache breaker: file is suffixed with .env tag
 * CSS then is updated to reference this specific file
 */

const tag = process.env.VERSION_TAG;
if ( tag === "6.x" ){
	log(red(`!-!-! Must set the correct git tag, not: ${tag}`));
	return;
} else {
	log( green( tag ));
}

const config = {
	iconPath: './src/icons-events/76x76/',
	glob: '*.png',
	png: `oe-i-e-76x76-${tag}.png`,
	scss: './src/sass/openeyes/ui/iconography/oe-i-e'
}

/**
 * Use Jimp to optimise PNG and write it out, imagemin had dependency issues.
 * @param {image Buffer} buffer
 */
const spritePNG = ( buffer ) => {
	// create new PNG
	// https://www.npmjs.com/package/jimp
	Jimp.read(buffer)
	.then(image => {
		// Do stuff with the image...these settings seem to generate smallest file size
		image
		.filterType(Jimp.PNG_FILTER_NONE) // set the filter type for the saved PNG
		.deflateLevel(8) // set the deflate level for the saved PNG
		.deflateStrategy(0) // set the deflate for the saved PNG (0-3)
		.write(`./dist/img/${config.png}`); // write out

		log(green('>>> png created: ') + config.png);
		log(green('(needs copying over to iDG)'));
	})
	.catch(err => log(red('Jimp error: ') + err));
};

/**
 * sass template for sprites
 * @returns String;
 */
const sassTemplate = ( sprite, sheetSize ) => {
	const name = sprite[0].slice(config.iconPath.length, -4);
	const { x, y } = sprite[1];
	// sass, not css
	return [
		`.i-${name} {`,
		`background-position: ${0 - x / 4}px ${0 - y / 4}px;`,
		`&.large { background-position: ${0 - x / 2}px ${0 - y / 2}px; }`,
		`}`,
		``,
	].join('\n');
}

/**
 * Build the SCSS sprite position
 * The icons are 76px x 76px, but used by CSS at 50% and at 25% e.g. 38px & 19px
 * @param {Object} : Object mapping filename to {x, y, width, height} of image
 * @param {Object} : Object with metadata about spritesheet {width, height}
 */
const buildSCSS = ( coordinates, properties ) => {
	const comments = [
		'// --- Automatically generated SCSS file by SpriteSmith',
		'// --- Do NOT edit this file directly: see node_scripts/build-iconset-events.js ',
		'',
	].join('\n');

	// background sizing for the sprite sheet
	const sheetSize = {
		quarter: {
			w: properties.width / 4,
			h: properties.height / 4
		},
		half: {
			w: properties.width / 2,
			h: properties.height / 2
		}
	};

	// build all the Sass classes
	let sassContents = [comments];
	Object.entries(coordinates).forEach(sprite => {
		// build file contents, using appropriate template
		sassContents.push(sassTemplate(sprite, sheetSize));
	});

	/** write out spritesheet */
	fs.writeFile(`${config.scss}/_oe-i-e-spritesheet.scss`, sassContents.join(''), err => {
		if ( err ){
			log(red('scss error: ') + err);
		} else {
			log(green('>>> _oe-i-e-spritesheet.scss: ') + config.scss);
		}
	})

	/** write out base */
	fs.writeFile(`${config.scss}/_oe-i-e-base.scss`, [
		comments,
		'@use "openeyes/var" as *;',
		'.oe-i-e {',
		`background: center / ${sheetSize.quarter.w}px ${sheetSize.quarter.h}px url("../img/${config.png}") no-repeat;`,
		'width: $oe-event-icon-size;',
		'height: $oe-event-icon-size;',
		'&.large {',
		`\tbackground-size: ${sheetSize.half.w}px ${sheetSize.half.h}px;`,
		'\twidth: #{$oe-event-icon-size + $oe-event-icon-size};',
		'\theight: #{$oe-event-icon-size + $oe-event-icon-size};',
		'}}'
	].join('\n'), err => {
		if ( err ){
			log(red('scss error: ') + err);
		} else {
			log(green('>>> _oe-i-e-base.scss: ') + config.scss);
		}
	});
};

/**
 * OK lets go...
 * Remove PNG and initiate Spritesmith
 */
rm(config.png, { force: true })
.then(() => {
	log('Current .png trashed...');
	// https://github.com/twolfson/spritesmith
	Spritesmith.run({
		src: fg.sync(`${config.iconPath}${config.glob}`) // use fast-glob here to generate an Array
	}, function handleResult( err, result ){
		if ( err ){
			log(red('Spritesmith error: ') + err);
			return;
		}
		log(`Spritesmith pulling from... "${config.iconPath}"`);
		log('Rebuild nxblu CSS if successful and check on iDG that they look OK!\n');
		// build the PNG
		spritePNG(result.image); // Buffer representation of image
		buildSCSS(result.coordinates, result.properties);
	});
});